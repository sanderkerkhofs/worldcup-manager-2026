type I18nContextValue = {
  locale: LocaleCode;
  setLocale: (locale: LocaleCode) => void;
  t: (key: string, replacements?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function isSupportedLocale(value: string): value is LocaleCode {
  return SUPPORTED_LOCALES.includes(value as LocaleCode);
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<LocaleCode>('en');

  useEffect(() => {
    const storedLocale = typeof window !== 'undefined' ? window.localStorage.getItem(STORAGE_KEY) : null;

    if (storedLocale && isSupportedLocale(storedLocale)) {
      setLocaleState(storedLocale);
      return;
    }

    const browserLocale = typeof navigator !== 'undefined' ? navigator.language.slice(0, 2).toLowerCase() : '';

    if (isSupportedLocale(browserLocale)) {
      setLocaleState(browserLocale);
    }
  }, []);

  useEffect(() => {
    if (typeof window !== 'undefined') {
      window.localStorage.setItem(STORAGE_KEY, locale);
    }

    if (typeof document !== 'undefined') {
      document.documentElement.lang = locale;
    }
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => ({
    locale,
    setLocale: setLocaleState,
    t: (key, replacements) => {
      const source = translations[locale][key] ?? translations.en[key] ?? key;

      if (!replacements) {
        return source;
      }

      return Object.entries(replacements).reduce(
        (acc, [replacementKey, replacementValue]) => acc.replaceAll(`{{${replacementKey}}}`, String(replacementValue)),
        source,
      );
    },
  }), [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);

  if (!context) {
    throw new Error('useI18n must be used within I18nProvider');
  }

  return context;
}